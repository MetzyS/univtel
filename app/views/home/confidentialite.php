    <h3>2. Pratiques de divulgation</h3>
    <p>Nous divulguons des informations personnelles si nous y sommes tenus par la loi ou par une ordonnance du tribunal, en réponse à un organisme d'application de la loi, dans la mesure permise par d'autres dispositions de la loi, pour fournir des informations, ou pour une enquête sur une question liée à la sécurité publique.</p>
    <p>Si notre site web ou notre organisation est repris, vendu ou impliqué dans une fusion ou une acquisition, vos données peuvent être divulguées à nos conseillers et à tout acheteur potentiel et seront transmises aux nouveaux propriétaires.</p>
