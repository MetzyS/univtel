    <h1 class="title">Déclaration de confidentialité</h1>

    <p>Cette déclaration de confidentialité a été mise a jour le 13 juin 2023 et s'applique aux citoyens et aux résidents permanents légaux de l'Espace économique européen et de la Suisse.</p>

    <p>Dans cette déclaration de confidentialité, nous expliquons ce que nous faisons avec les données que nous obtenons à votre sujet via https://www.univtel.fr. Nous vous recommandons de lire attentivement cette déclaration. Lors de notre traitement, nous nous conformons aux exigences de la législation sur la confidentialité. Cela signifie, entre autres, que :</p>

    <ul>
        <li>nous indiquons clairement dans quelles finalités nous traitons les données personnelles. Nous faisons cela au moyen de cette déclaration de confidentialité;</li>
        <li>nous visons à limiter notre recueil de données personnelles uniquement aux données personnelles nécessaires à des finalités légitimes;</li>
        <li>nous demandons d'abord votre consentement explicite pour traiter vos données personnelles dans les cas nécessitant votre consentement;</li>
        <li>nous prenons des mesures de sécurité appropriées afin de protéger vos données personnelles;</li>
        <li>nous respectons votre droit de consulter, corriger ou supprimer vos données personnelles si vous en formulez la demande.</li>
    </ul>
